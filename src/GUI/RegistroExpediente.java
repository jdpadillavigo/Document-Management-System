    private void BGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BGrabarActionPerformed
        String id = this.tID.getText();
        String prio = this.tPrioridad.getText();
        String asunto = this.tAsunto.getText();
        String dref = this.tDocs.getText();
        Usuario oUsuario = (Usuario)this.cUsuario.getSelectedItem();
        Dependencia oDependencia = (Dependencia) this.cDependencia.getSelectedItem();
        String inicio = this.tFyH.getText();
        if (id.equalsIgnoreCase("") || prio.equalsIgnoreCase("") || asunto.equalsIgnoreCase("") || dref.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "¡Falta completar datos!");
        }
        else {
            if (esEntero(prio)) {
                int prioridad=Integer.parseInt(prio);
                Expediente oExpediente = new Expediente(id,prioridad,oUsuario,asunto,dref,oDependencia,inicio);
                this.RExpedientes.encolar(oExpediente);
            
                this.tID.setText(null);
                this.tPrioridad.setText(null);
                this.tAsunto.setText(null);
                this.tDocs.setText(null);
                JOptionPane.showMessageDialog(null, "¡Se registró exitosamente!");
            }
            else {
                JOptionPane.showMessageDialog(null, "¡La prioridad asignada debe ser un número entero!");
            }
        }
    }//GEN-LAST:event_BGrabarActionPerformed

