    private void llenarUsuarios() {
        Lista<Usuario> listaUsuarios = this.Registro.obtenerListaUsuarios();
        Nodo<Usuario> aux = listaUsuarios.getCabeza();
        while (aux != null) {
            Usuario ObjUsuario = aux.getElemento();
            this.modeloComboUsuario.addElement(ObjUsuario);
            aux = aux.getSgteNodo();
        }
    }
    
    private void llenarDependencias() {
        Lista<Dependencia> listaDependencias = this.RDependencias.obtenerListaDependencias();
        Nodo<Dependencia> aux = listaDependencias.getCabeza();
        while (aux != null) {
            Dependencia ObjDependencia = aux.getElemento();
            this.modeloComboDependencia.addElement(ObjDependencia);
            aux = aux.getSgteNodo();
        }
    }
    
    public boolean esEntero(String cadena) {
        if (cadena == null || cadena.isEmpty()) {
            return false;
        }

        for (int i = 0; i < cadena.length(); i++) {
            char caracter = cadena.charAt(i);
            if (i == 0 && (caracter == '+' || caracter == '-')) {
                continue;
            }
            if (!Character.isDigit(caracter)) {
                return false;
            }
        }

        return true;
    }

