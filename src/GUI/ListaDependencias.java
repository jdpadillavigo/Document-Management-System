    private void CargarTabla() {
        int filas=this.TablaDependencias.getRowCount();
        for (int i = 0; i < filas; i++) {
            modeloTabla.removeRow(0);
        }
        Lista<Dependencia> listaDependencia = this.RDependencias.obtenerListaDependencias();
        Nodo<Dependencia> aux = listaDependencia.getCabeza();
        int num=1;
        while (aux != null) {
            Dependencia ObjDependencia = aux.getElemento();
            String[] fila = new String[2];
            fila[0] = String.valueOf(num);
            fila[1] = ObjDependencia.getNombre();
            modeloTabla.addRow(fila);
            aux = aux.getSgteNodo();
            num++;
        }
    }
    
