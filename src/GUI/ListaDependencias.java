    private void RegistroExpedienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistroExpedienteActionPerformed
        String nombre=JOptionPane.showInputDialog(null, "Ingrese el nombre de la Dependencia:");
        if (nombre.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(null, "¡No asignó un nombre!");
        }
        else {
            Dependencia oDependencia = new Dependencia(nombre);
            this.RDependencias.agregar(oDependencia);
            CargarTabla();
        }
    }//GEN-LAST:event_RegistroExpedienteActionPerformed

    private void EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarActionPerformed
        int fila=-1;
        fila = TablaDependencias.getSelectedRow();
        if (fila!=-1) {
            String aBuscar= TablaDependencias.getValueAt(fila,1).toString();
            this.RDependencias.eliminarPorNombre(aBuscar);
            CargarTabla();
        }
        else{
            JOptionPane.showMessageDialog(null,"¡Seleccione una fila!");
        }
    }//GEN-LAST:event_EliminarActionPerformed

